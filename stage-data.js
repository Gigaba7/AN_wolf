export function getStageCandidatesByChapterRange(minChapter, maxChapter) {
  const min = Number.isFinite(minChapter) ? minChapter : 0;
  const max = Number.isFinite(maxChapter) ? maxChapter : 0;
  const lo = Math.max(0, Math.min(15, Math.floor(Math.min(min, max))));
  const hi = Math.max(0, Math.min(15, Math.floor(Math.max(min, max))));

  /** @type {string[]} */
  const out = [];
  for (let ch = lo; ch <= hi; ch++) {
    const arr = STAGES_BY_CHAPTER[ch];
    if (Array.isArray(arr) && arr.length) out.push(...arr);
  }
  return out;
}

